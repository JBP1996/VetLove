package controller;

import java.util.Date;

/**
 *
 * @author JBP1996 Atec
 */
public class Vacina{
    private Date data;
    private String nome;
    private double preco;

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
}
